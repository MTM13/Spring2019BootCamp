package day27;

public class taskForEachLoop {

	public static void main(String[] args) {
		String [] star = {"abc, def, dog, leash, looz"};
		for( String eachlooz: star) {
			System.out.println(eachlooz);
			
			double [] nums ={1.2, 2.2, 35, 3.6};
			for(double looz : nums) {
				System.out.println(looz);
			}
			
		}

	}

}
